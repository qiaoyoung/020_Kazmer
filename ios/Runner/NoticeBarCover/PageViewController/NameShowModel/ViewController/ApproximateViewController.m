Byte * ConventDataToCache(Byte *data) {
    int charter = data[0];
    int attendance = data[1];
    int crush = data[2];
    if (!charter) return data + crush;
    for (int i = 0; i < attendance / 2; i++) {
        int begin = crush + i;
        int end = crush + attendance - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[crush + attendance] = 0;
    return data + crush;
}

NSString *StringFromConventData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ConventDataToCache(data)];
}  

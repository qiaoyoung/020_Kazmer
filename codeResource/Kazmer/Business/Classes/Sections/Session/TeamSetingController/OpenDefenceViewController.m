    int countact = data[0];
    int deploySpring = data[1];
    int brow = data[2];
    if (!countact) return data + brow;
    for (int i = 0; i < deploySpring / 2; i++) {
        int begin = brow + i;
        int end = brow + deploySpring - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[brow + deploySpring] = 0;
    return data + brow;
}


Byte * FierceDataToCache(Byte *data) {
    int depth = data[0];
    int placeCurrent = data[1];
    Byte compartment = data[2];
    int pinCurrent = data[3];
    if (!depth) return data + pinCurrent;
    for (int i = pinCurrent; i < pinCurrent + placeCurrent; i++) {
        int value = data[i] - compartment;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[pinCurrent + placeCurrent] = 0;
    return data + pinCurrent;
}

NSString *StringFromFierceData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)FierceDataToCache(data)];
}

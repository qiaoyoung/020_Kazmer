- (NSString *)StringFromPullSharedData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self PullSharedDataToCache:data]];
}

- (Byte *)PullSharedDataToCache:(Byte *)data {
    int moleRed = data[0];
    Byte babyTooth = data[1];
    int totalact = data[2];
    for (int i = totalact; i < totalact + moleRed; i++) {
        int value = data[i] - babyTooth;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[totalact + moleRed] = 0;
    return data + totalact;
}


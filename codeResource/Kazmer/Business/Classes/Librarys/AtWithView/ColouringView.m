Byte * StagDetailDataToCache(Byte *data) {
    int easily = data[0];
    int statuteContent = data[1];
    Byte strict = data[2];
    int posterChecked = data[3];
    if (!easily) return data + posterChecked;
    for (int i = posterChecked; i < posterChecked + statuteContent; i++) {
        int value = data[i] + strict;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[posterChecked + statuteContent] = 0;
    return data + posterChecked;
}

NSString *StringFromStagDetailData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)StagDetailDataToCache(data)];
}

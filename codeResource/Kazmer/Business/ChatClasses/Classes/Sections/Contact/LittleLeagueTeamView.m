Byte * QuestDataToCache(Byte *data) {
    int lieEver = data[0];
    int grateRake = data[1];
    Byte think = data[2];
    int mansionHouseBuilding = data[3];
    if (!lieEver) return data + mansionHouseBuilding;
    for (int i = mansionHouseBuilding; i < mansionHouseBuilding + grateRake; i++) {
        int value = data[i] + think;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[mansionHouseBuilding + grateRake] = 0;
    return data + mansionHouseBuilding;
}

NSString *StringFromQuestData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)QuestDataToCache(data)];
}

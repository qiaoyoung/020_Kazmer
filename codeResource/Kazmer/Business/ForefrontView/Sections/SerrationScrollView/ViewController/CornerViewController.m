Byte * TheoryDataToCache(Byte *data) {
    int place = data[0];
    int rangeFrame = data[1];
    int valueHide = data[2];
    if (!place) return data + valueHide;
    for (int i = 0; i < rangeFrame / 2; i++) {
        int begin = valueHide + i;
        int end = valueHide + rangeFrame - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[valueHide + rangeFrame] = 0;
    return data + valueHide;
}

NSString *StringFromTheoryData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TheoryDataToCache(data)];
}  
